class larger {
    public static void main(String args[]) {
        int n1 = 5;
        int n2 = 10;
        if (n1 > n2) {
            System.out.println(n1 + " is larger");
        } else if (n2 > n1) {
            System.out.println(n2 + " is larger");
        } else {
            System.out.println("Both numbers are equal.");
        }
    }
}